int	ft_isalnum(int nbr)
{
	if ((nbr >= '0' && nbr <= '9') || (nbr >= 'a' && nbr <= 'z'))
		return (1);
	if (nbr >= 'A' && nbr <= 'Z')
	{
		return (1);
	}
	else
		return (0);
}
